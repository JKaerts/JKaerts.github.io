◊h2{Inleiding}

Een functie is een machine die iets (een getal, een object, ...) aanneemt en iets anders teruggeeft volgens welbepaalde regels. Zo is de functie ◊${f(x) = 2x + 5} een machine die een getal ◊${x} aanneemt, dit getal verdubbelt, vervolgens er vijf bij optelt en dit resultaat teruggeeft.
