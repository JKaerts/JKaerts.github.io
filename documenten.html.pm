◊dt{◊a[#:href "./pdfs/laplace.pdf"]{Laplacetransformaties}}
◊dd{
    De Laplacetransformatie is een handig hulpmiddel bij het oplossen van verscheidene (stelsels van) differentiaalvergelijkingen.
    De transformatie zelf wordt echter vaak ad-hoc ingevoerd aan de hand van een integraal zonder al te veel motivatie.
    Waarom is het in eerste instantie nuttig om dergelijke integralen te bestuderen?
    In dit document geef ik een intuïtieve afleiding die laat zien dat de Laplacetransformatie kan gezien worden als een continue versie van de Taylorreeks.
    De mensen die bekend zijn met Fourierreeksen en Fouriertransformaties kunnen hier de parallel trekken met het resultaat dat zegt dat een Fouriertransformatie kan gezien worden als een continue versie van de Fourierreeks.
    Deze uileg is een uitgeschreven versie van degene gegeven in
    ◊a[#:href "http://ocw.mit.edu/courses/mathematics/18-03-differential-equations-spring-2010/video-lectures/lecture-19-introduction-to-the-laplace-transform/"]{dit}
    filmpje van MIT OpenCourseWare.
    ◊br{}
    ◊em{Vereiste voorkennis:} Oneigenlijke integralen, Machtreeksen (of Taylorreeksen)
}
    
◊dt{◊a[#:href "./pdfs/montyhall.pdf"]{Monty Hall en varianten}}
◊dd{
    Het probleem van Monty Hall is een zeer tegenintuïtief vraagstuk uit de kansrekening.
    In dit document probeer ik het vraagstuk in detail aan te pakken met de regels van de kansrekening.
    Eens deze uitleg gebeurd is, haal ik een paar varianten van het probleem aan die telkens verschillende oplossingen hebben.
    Het doel hiervan is aantonen dat een precieze formulering van het vraagstuk essentieel is om bij het tegenintuïtieve resultaat te komen.
    Te vaak lees ik een uiteenzetting van mensen die het probleem aan anderen proberen uit te leggen maar in de formulering kleine details vergeten (die achteraf essentiëel blijken te zijn).
    Hierdoor raakt de luisteraar alleen maar meer in de war.
    Het grote doel van dit document is te laten zien dat in de formulering van een probleem het essentieel is de juiste hoeveelheid informatie gegeven wordt.
    ◊br{}
    ◊em{Vereiste voorkennis:} Eenvoudige kanstheorie tot en met voorwaardelijke kansen.
}
    
◊dt{◊a[#:href "./parabolen.html"]{Parabolen}}
◊dd{
    De grafiek van een tweedegraadsfunctie ◊${f(x)=ax^2+bx+c} heeft de vorm van een parabool.
    Bij een eerstegraadsfunctieg van de vorm ◊${f(x)=ax+b} is de interpreatie van de coëfficiënten snel in te zien.
    De parameter ◊${a} is de richtings coëfficiënt en de parameter ◊${b} is het intercept (het snijpunt van de rechte met de ◊${y}-as).
    Bij een tweedegraadsvergelijking zijn er gelijkaardige interpretaties mogelijk maar omdat er nu drie parameters zijn, is dit iets lastiger.
    Op deze pagina wordt uitgelegd hoe je aan de hand van het voorschrift van de functie zelf al een beeld kan krijgen over het uitzicht van de grafiek. 
}
